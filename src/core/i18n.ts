export const STRINGS: Record<LangCode, typeof UK> = { uk: UK, en: EN }

export type StringKey = keyof typeof UK

export function t(
  lang: LangCode,
  key: StringKey,
  params?: Record<string, string | number>
): string {
  let str = STRINGS[lang]?.[key] ?? STRINGS.uk[key] ?? key
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(`{{${k}}}`, String(v))
    }
  }
  return str
}
